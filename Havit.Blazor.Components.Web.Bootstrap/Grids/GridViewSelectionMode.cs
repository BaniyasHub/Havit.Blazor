namespace Havit.Blazor.Components.Web.Bootstrap.Grids
{
	/// <summary>
	/// GridView selection mode.
	/// </summary>
	public enum GridViewSelectionMode
	{
		/// <summary>
		/// No selection is performed.
		/// </summary>
		None,

		/// <summary>
		/// One item can be selected.
		/// </summary>		
		Select,

		/// <summary>
		/// Multiple items can be selected.
		/// </summary>
		MultiSelect
	}
}